public class ConversaoUnidades {
    public static double metroQuadrado(int metro){
        return metro * 10.76;
    }
    
    public static int peQuadrado(int peQua){
        return peQua * 929;
    }
    
    public static int milhaQuadrada(int milha){
        return milha * 640;
    }
    
    public static double acre (int acrePassado){
        return acrePassado * 43.560;
    }
    
    
}
